    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $usuario = Usuario::find($id);
        $usuario->delete();
        
        $usuarios = DB::table('_usuarios')->get();
        return view('Usuarios.index', ['usuarios' => $usuarios]);


